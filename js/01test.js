let i = 12;
console.log(`type de i : `, typeof(i));

i = "Hello";
console.log(`type de i : `, typeof(i));

i = true;
console.log(`type de i : `, typeof(i));


i = null;
console.log(`type de i : `, typeof(i));

i = undefined;
console.log(`type de i : `, typeof(i));

i = 0;
console.log(`type de i : `, typeof(i));
i = !!i;
console.log(`type de i : `, typeof(i));
